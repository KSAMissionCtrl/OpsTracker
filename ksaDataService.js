  // ===========================================================================
  // PUBLIC API — INDEX MANAGEMENT
  // ===========================================================================

  /**
   * loadIndex(entityId)
   *
   * Loads and caches the index file for a craft or kerbal entity (once per page
   * load).  Subsequent calls for the same entityId are served from the index
   * cache with no network round-trip.
   *
   * The index object maps recordset folder names to sorted UT arrays:
   *   { craftdata: [0, 28800], flightdata: [0, 28800, 57600], ... }
   *
   * @param {string} entityId  Craft DB name or kerbal ID (lowercase).
   * @returns {Promise<Object>}
   */
  function loadIndex(entityId) {
    if (_indexCache.has(entityId)) {
      return Promise.resolve(_indexCache.get(entityId));
    }
    return fetchJson(indexPath(entityId)).then(function (index) {
      _indexCache.set(entityId, index);
      return index;
    });
  }

  // ===========================================================================
  // PHASE 3 HELPERS
  // ===========================================================================

  /**
   * _normalizeStatsRecord(stats)
   *
   * Coerces Dockings and TEVA to proper integers.
   * Older DB exports serialized zero values as the string "0" rather than the
   * integer 0.  String "0" is truthy in JS, which would cause the conditional
   * display guards in ksaCrewOps.js to incorrectly show empty rows.
   * Operates in-place; safe to call with null/undefined.
   */
  function _normalizeStatsRecord(stats) {
    if (!stats) return;
    if (stats.Dockings !== null && stats.Dockings !== undefined) stats.Dockings = +stats.Dockings || 0;
    if (stats.TEVA     !== null && stats.TEVA     !== undefined) stats.TEVA     = +stats.TEVA     || 0;
  }

  /**
   * _normalizeKeys(obj)
   *
   * Returns a shallow copy of obj with all spaces removed from key names.
   * e.g. "Orbital Period" → "OrbitalPeriod", "SOI Event" → "SOIEvent".
   * Returns null/undefined unchanged.
   */
  function _normalizeKeys(obj) {
    if (!obj || typeof obj !== 'object') return obj;
    var normalized = {};
    Object.keys(obj).forEach(function (key) {
      normalized[key.replace(/ /g, '')] = obj[key];
    });
    return normalized;
  }

